В битовото християнство се шири вярването, че свети Андрей е покровител на мечката 
и неин заповедник. В някои български райони наричат деня още Едрей, Едринден или 
Мечкин ден. Според народните възгледи и познания от този ден започва нарастването 
на деня - поговорка гласи: "На Едрей денят започва да наедрява колкото едно 
просено (житно, маково или синапено) зърно."
