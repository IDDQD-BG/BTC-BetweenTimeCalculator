Българският обичай Сполязуване(1), Споляз, Сполез (по-известен в опростената си форма Полязуване, Поляз, Полез) 
има много древен, езически и предхристиянски характер и се празнува на 21 декември - денят Единак или Еднажден, 
християнизиран като Игнажден, ден на св. Игнатий. Този ден се нарича още Полязовден или Сполязинден.
