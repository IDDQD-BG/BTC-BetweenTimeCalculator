According to the Bulgarian traditional calendar this day is named <a href="https://en.wikipedia.org/wiki/Survakane">Surovaki or Survaki</a> (also <i>Vasilyovday</i>). <br/>
The supposed prototype of this day is is the day that was the first day of the first month of the year under the Ancient Bulgarian Calendar. 
This day was just after the day Eni (equivalent to Ignazhden/Ednazhden).
According to  
<a href="links/daobgkalendar.pdf">
Georgi Velev</a>, the adoption of the 
<a href="https://en.wikipedia.org/wiki/Julian_calendar">
Julian calendar</a> (later replaced by the <a href="https://en.wikipedia.org/wiki/Gregorian_calendar">Gregorian calendar</a>) and the abandonment of the Ancient Bulgarian Calendar, 
the Surovaki selebration has kept its numeric value 01-01 (first day of the First Month), but has moved with ten days due to the difference in the beginning of the year under the Ancient Bulgarian Calendar and the Julian calendar.
<br/><br/>
Georgi Velev finds a connection in the essence of the traditiona in that day  and the Eastern idea of the life force "ki". According to him, the name "Surovaki" literally means "raw ki" (<a href="https://translate.google.com/#el/en/%D1%81%D1%83%D1%80%D0%BE%D0%B2%D0%B0">"surova"</a> means row in Bulgarian), because it is at this point that the new circle of vital energy begins.
