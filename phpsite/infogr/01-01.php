Laut dem bulgarischen Volkskalender - <a href="https://de.wikipedia.org/wiki/Surwa">Surrowaki/Surwaki</a> (oder Vasilyovden).
Der angebliche Prototyp dieses Tages ist der Tag, der der erste Tag des ersten Monats des Jahres unter dem alten bulgarischen Kalender war.
Dieser Tag war nach dem Tag Eni (gleichwertig Ignazhden/Ednazhden).
Laut <a href="links/daobgkalendar.pdf">Georgi Welew</a>, die Annahme des <a href="https://de.wikipedia.org/wiki/Julianischer_Kalender">Julianischen Kalenders</a> (später durch den <a href="https://de.wikipedia.org/wiki/Gregorianischer_Kalender">Gregorianischen Kalender</a> ersetzt) und die Aufgabe des alten bulgarischen Kalenders, hat der Surva-Feiertag seinen Zahlenwert von 01-01 (erster Tag des ersten Monats) beibehalten, hat aber Er bewegte sich zehn Tage lang wegen des Unterschieds zu Beginn des Jahres auf dem antiken bulgarischen Kalender und dem julianischen Kalender. 
<br/><br/>
Georgi Velev findet Verbindung im Wesen des Festivals und die östliche Idee der Lebenskraft "ki". Ihm zufolge bedeutet der Name "<a href="https://translate.google.com/#el/de/%D1%81%D1%83%D1%80%D0%BE%D0%B2%D0%B0">surowa</a>ki" wörtlich "roh ki", weil in diesem Moment der neue Zyklus der Lebensenergie beginnt.
