<h3>What typeface of Cyrillic do we call a typical Bulgarian</h3>

The site <a href="http://bgkalendar.com">The Bulgarian Calendar</a> participates in the initiative to make the typical Bulgarian typeface of Cyrillic fonts more popular.<br/><br/> 
Read more about what we call Bulgarian typeface of Cyrillic fonts on the following site: <a href="http://cyrillic.bg">For the Bulgarian Cyrillic</a> 
or in <a href="https://www.spisanie8.bg/%D1%80%D1%83%D0%B1%D1%80%D0%B8%D0%BA%D0%B8/%D0%B8%D1%81%D1%82%D0%BE%D1%80%D0%B8%D1%8F/3435-%D0%B7%D0%B0-%D0%B1%D1%8A%D0%BB%D0%B3%D0%B0%D1%80%D1%81%D0%BA%D0%B0%D1%82%D0%B0-%D0%BA%D0%B8%D1%80%D0%B8%D0%BB%D0%B8%D1%86%D0%B0.html">this post from Ossem magazine</a>.<br/>
In short Cyrillic fonts with typical Bulgarian typeface are typical and most wide spread in Bulgaria.

They are distinguished with handwritten type of delineation of lower case letters and some major letters, unlike the Cyrillic fonts that are typical for Russia, where small letters have the same form as the big ones - just smaller.
<br/><br/>
Because of the bigger variety of forms - the Bulgarian typeface of Cyrillic is easier and more pleasant to read.
<br/><br/>
Recently, it became increasingly used and preferred.
<br/><br/>
<a href="images/kupuлuцa-en.png"><img src="images/kupuлuцa-en.png" style="max-width: 100%;"/></a><br/><br/>
We include our site in the initiative for the promotion of the Bulgarian typeface of Cyrillic alphabet, as well as give suggestions for free fonts with Bulgarian typeface of Cyrillic, that you can download and use in your Web pages, documents and projects.

