<h3>Welcher kyrillisch Schriftart nennen wir typisch Bulgarisch</h3>

Die Seite <a href="http://bgkalendar.com/?lang=de">"Der Bulgarisch Kalender"</a> unterstützt die Initiative für Verbreitung auf den typisch bulgarisch kyrillisch Schriftarten. 
<br/><br/> 
Weitere Information bezüglich was bezeichnen wir bulgarisch Kyrilliza, finden Sie in der Seite der Initiative  
<a href="http://cyrillic.bg/">"Zu Bulgarisch Kyrilliza"</a>
oder in <a href="https://www.spisanie8.bg/%D1%80%D1%83%D0%B1%D1%80%D0%B8%D0%BA%D0%B8/%D0%B8%D1%81%D1%82%D0%BE%D1%80%D0%B8%D1%8F/3435-%D0%B7%D0%B0-%D0%B1%D1%8A%D0%BB%D0%B3%D0%B0%D1%80%D1%81%D0%BA%D0%B0%D1%82%D0%B0-%D0%BA%D0%B8%D1%80%D0%B8%D0%BB%D0%B8%D1%86%D0%B0.html">diesem Artikel in "Ossem" Zeitschrift</a>.<br/>
Kurtz gesagt, typisch bulgarisch kyrillisch Schriftarten werden am meisten im Bulgarien benutzt und sie sind typisch für Bulgarien.
Sie zeichnen sich durch mehr handschriftlich Schrift der kleinen Buchstaben und einige der großen Buchstaben. 
Das ist im Unterschied zu den typisch für Russland, wo Кleinбuchstaben sind fast immer geringe Abbild der Großbuchstaben.
<br/><br/>
Wegen der größeren Variantenvielfalt, ist die typische bulgarische kyrillisch Schriftart einfacher und angenehmer zu lesen. 
<br/><br/>
In der letzten Zeit, sie wird häufiger verwendet und bevorzugt.
<br/><br/>
<a href="images/kupuлuцa-de.png"><img src="images/kupuлuцa-de.png" style="max-width: 100%;"/></a><br/><br/>
Unsere Webseite mitmacht in der Initiative zur Förderung der typisch bulgarisch kyrillisch Schriftarten.
Wir geben Ihnen auch Vorschläge für kostenlose bulgarisch Schriftarten, die können Sie herunterladen un in Ihren Webseiten, Dokumente und Proekte verwenden.   

