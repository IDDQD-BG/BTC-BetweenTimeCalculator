<h3>Какие шрифты кириллицы называем типичными болгарскими</h3>
Веб-страница <a href="http://bgkalendar.com/?lang=ru">&#xab;Болгарский Календарь&#xbb;</a> включена в инициативу распространения типичного болгарского начертания кириллических шрифтов.  <br/><br/>

Подробнее о болгарском алфавите можете прочитать на веб-сайте инициативы <a href="http://cyrillic.bg/">&#xab;Для Българской Кириллицы&#xbb;</a> или в <a href="https://www.spisanie8.bg/%D1%80%D1%83%D0%B1%D1%80%D0%B8%D0%BA%D0%B8/%D0%B8%D1%81%D1%82%D0%BE%D1%80%D0%B8%D1%8F/3435-%D0%B7%D0%B0-%D0%B1%D1%8A%D0%BB%D0%B3%D0%B0%D1%80%D1%81%D0%BA%D0%B0%D1%82%D0%B0-%D0%BA%D0%B8%D1%80%D0%B8%D0%BB%D0%B8%D1%86%D0%B0.html">следующей статье в Журнале 8</a>. &#xab;Коротко о типичных для болгарской кириллицы шрифтов, которые используются наиболее широко в Болгарии.&#xbb; Отличается она тем, что буквы нижнего регистра и некоторые буквы верхнего более &#xab;рукописные&#xbb; в начертании, в отличии от кириллических шрифтов, типичные для России, где прописные буквы являются копием заглавных.  
<br/><br/>
Благодаря разнообразию форм типичный болгарский шрифт является легким и приятным для чтения.  
<br/><br/>
В последнее время, он становится всё более распространенным и предпочтительным.  
